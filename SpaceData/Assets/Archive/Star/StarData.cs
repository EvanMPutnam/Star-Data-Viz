

public class StarData{


	private float xLoc;
	public float XLoc
	{
		get{return xLoc;}
	}

	private float yLoc;
	public float YLoc
	{
		get{ return yLoc; }
	}
		
	private float zLoc;
	public float ZLoc
	{
		get{ return zLoc; }
	}


	public StarData(float xLoc, float yLoc, float zLoc){
		this.xLoc = xLoc;
		this.yLoc = yLoc;
		this.zLoc = zLoc;
	}
		
}
